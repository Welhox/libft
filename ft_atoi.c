int	ft_atoi(const char *str)

{
	int	num;
	int	i;

	i = 0;
	num = 0;
	if (str[0] == '-')
		i++;
	while (str[i] >= 48 && str[i] <= 57)
	{
		num = (num * 10) + (str[i] - 48);
		i++;
	}
	if (str[0] == '-')
		num *= -1;
	return (num);
}
